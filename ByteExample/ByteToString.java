package ByteExample;

public class ByteToString {


    private final static int intToByte = 3;
    private final static char charToByte = 'C';


    public static void main(String[] args) {
        

        System.out.println(Integer.toBinaryString(intToByte));
        System.out.println(Integer.toBinaryString(charToByte));


    }

}
